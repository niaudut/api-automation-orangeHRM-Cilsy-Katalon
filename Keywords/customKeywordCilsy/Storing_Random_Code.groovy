public class Storing_Random_Code {

	public static String employee_code

	@Keyword
	def Generate_Code(){
		Random random = new Random();
		String firstCharCode = "EM";
		String lastCharCode = String.format('%02d', random.nextInt(899) + 100);
		String code_employee = firstCharCode+lastCharCode
		this.employee_code = code_employee
		return employee_code
	}
